package com.bit2020.chapter04;

public class StringTest01 {
    
	
	
	public static void main(String[] args) {
		// c:\temp
		String s = "c:\\temp";
		System.out.println(s);
		
		// "hello"
		System.out.println("\"hello\"");
		
		// 제어문자
		// \t -> tab
		// \n -> new line(개행)
		// \r -> carriage return
		System.out.println("hello");
		System.out.print("hello\n");
		System.out.print("hello\r\n");

		char c = '\'';
	}

}
